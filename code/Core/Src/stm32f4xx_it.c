/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  static unsigned int Rcount = 0;
  static unsigned int Rcount_times = 0;
  if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_RXNE)) {
    car.Recive_Data.buffer[Rcount] = huart2.Instance->DR;
    (car.Recive_Data.buffer[0] == 0xFe) ? (Rcount++) : (Rcount = 0);
    if (Rcount == PROTOCL_DATA_SIZE) {
      if (car.Recive_Data.Sensor_Str.Header == PROTOCOL_HEADER) {
        if (car.Recive_Data.Sensor_Str.End_flag == PROTOCOL_END) {
          Car_Kinematics_Positive(car.Recive_Data.Sensor_Str.X_speed, car.Recive_Data.Sensor_Str.Y_speed, car.Recive_Data.Sensor_Str.Z_speed);
          if ((car.Recive_Data.Sensor_Str.PID_Param[0] + car.Recive_Data.Sensor_Str.PID_Param[1] + car.Recive_Data.Sensor_Str.PID_Param[2]) != 0) {
            float KP = car.Recive_Data.Sensor_Str.PID_Param[0];
            float KI = car.Recive_Data.Sensor_Str.PID_Param[1];
            float KD = car.Recive_Data.Sensor_Str.PID_Param[2];
            Car_Update_Pid_Param(KP, KI, KD);
          }
          Rcount_times++;
          if (Rcount_times % 5 == 0) {
            led_on(100); // blinking while receive 5 packages;
          }
          car.Recive_Timer = 0;
        }
      }
      Rcount = 0;
    }
  }
  /* USER CODE END USART2_IRQn 0 */
  /* USER CODE BEGIN USART2_IRQn 1 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE END USART2_IRQn 1 */
}

